# max() que retorna o maior valor numérico dentre os elementos da lista;
# min(): que retorna o menor valor numérico dentre os elementos da lista;
# sum(): que retorna o total entre os valores que estão na lista;

valores = [12, 313, 44, 54, 3, 232, 32]

print(f"O maior número da lista é: {max(valores)}")
print(f"O menor número da lista é: {min(valores)}")
print(f"A soma de todos os números da lista é: {sum(valores)}")